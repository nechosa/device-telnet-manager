int QMessageDlg( msgDlgType dlgType, QWidget *parent, const QString &caption,
                   const QString& text, int button0, int button1, int button2 )
{
  QMessageBox::Icon icon = QMessageBox::NoIcon;
  const char * const * pix = NULL;

  switch (dlgType)
  {
    case mtWarning:
      icon = QMessageBox::Warning;
      pix = warning_pxm;
    break;
    case mtCritical:
      icon = QMessageBox::Critical;
      pix = error_pxm;
    break;
    case mtInformation:
      icon = QMessageBox::Information;
      pix = information_pxm;
    break;
    case mtConfirmation:
      icon = QMessageBox::Information;
      pix = confirmation_pxm;
    break;
    default:
      icon = QMessageBox::Icon(dlgType);
    break;
  }
 #if (QT_VERSION < 0x040000)
    QMessageBox *mb = new QMessageBox( caption, text, icon,
                                       button0, button1, button2,
                                       parent, "qt_msgbox", TRUE,
                                       Qt::WDestructiveClose);
   mb->setCursor( Qt::arrowCursor );
#else
    QMessageBox *mb = new QMessageBox( caption, text, icon,
                                       button0, button1, button2,
                                       parent);
    mb->setAttribute(Qt::WA_DeleteOnClose, true);
#endif
    Q_CHECK_PTR( mb );
     if (pix!=NULL)
      mb->setIconPixmap( QPixmap((const char**)pix) );

    int reply = mb->exec();
    return reply;
}

int QMessageDlg( msgDlgType dlgType, QWidget *parent, const QString &caption,
                            const QString& text,
                            const QString& button0Text,
                            const QString& button1Text,
                            const QString& button2Text,
                            int defaultButtonNumber,
                            int escapeButtonNumber )
{
  QMessageBox::Icon icon = QMessageBox::NoIcon;
  const char * const * pix = NULL;

  switch (dlgType)
  {
    case mtWarning:
      icon = QMessageBox::Warning;
      pix = warning_pxm;
    break;
    case mtCritical:
      icon = QMessageBox::Critical;
      pix = error_pxm;
    break;
    case mtInformation:
      icon = QMessageBox::Information;
      pix = information_pxm;
    break;
    case mtConfirmation:
      icon = QMessageBox::Information;
      pix = confirmation_pxm;
    break;
    default:
      icon = QMessageBox::Icon(dlgType);
    break;
  }

    int b[3];
    b[0] = 1;
    b[1] = button1Text.isEmpty() ? 0 : 2;
    b[2] = button2Text.isEmpty() ? 0 : 3;

    int i;
    for( i=0; i<3; i++ ) {
        if ( b[i] && defaultButtonNumber == i )
            b[i] += QMessageBox::Default;
        if ( b[i] && escapeButtonNumber == i )
            b[i] += QMessageBox::Escape;
    }

#if (QT_VERSION < 0x040000)
   QMessageBox *mb = new QMessageBox( caption, text, icon,
                                      b[0], b[1], b[2],
                                      parent, "qt_msgbox", TRUE,
                                      Qt::WDestructiveClose);

   mb->setCursor( Qt::arrowCursor );
#else
   QMessageBox *mb = new QMessageBox( caption, text, icon,
                                      b[0], b[1], b[2],
                                      parent);
   mb->setAttribute(Qt::WA_DeleteOnClose, true);
#endif

    Q_CHECK_PTR( mb );
    if ( button0Text.isEmpty() )
        mb->setButtonText( 1, QString::fromLocal8Bit("Закрыть") );
    else
        mb->setButtonText( 1, button0Text );
    if ( b[1] )
        mb->setButtonText( 2, button1Text );
    if ( b[2] )
        mb->setButtonText( 3, button2Text );
    if (pix!=NULL)
      mb->setIconPixmap( QPixmap((const char**)pix) );
    int reply = mb->exec();
    return reply-1;
}
